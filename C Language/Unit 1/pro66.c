/**
    Relational Operator
    0 = False, 1 = True

    >   greater than
    >=  greater than or  equals to
*/

#include<stdio.h>

void main(){
    printf("\n Answer is %d", (11 > 10));
    printf("\n Answer is %d", (101 > 110));
    printf("\n Answer is %d", (110 > 101));
    printf("\n Answer is %d", (10 > 10));
    printf("\n Answer is %d", (10 >= 10));
}
